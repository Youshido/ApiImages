namespace Youshido\ImagesBundle\DTO;


use Youshido\ImagesBundle\Document\Interfaces\PathableInterface;

class PathableDTO implements PathableInterface
{

    private $path;

    public function __construct($path)
    {
        $this->path = $path;
    }

    /**
     * @return string
     */
    public function getPath()
    {
        return $this->path;
    }
}